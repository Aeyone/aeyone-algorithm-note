void solve() {
	int l, r;
	cin >> l >> r;
	int odd = l + !(l & 1);
	if(2 * odd <= r){
		if(l & 1){
			cout << "Alice" << '\n';
		}else{
			cout << "Bob" << '\n';
		}
	}else{
		if((r - l + 1) & 1){
			cout << "Alice" << '\n';
		}else{
			cout << "Bob" << '\n';
		}
	}
}

