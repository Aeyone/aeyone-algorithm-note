#include <bits/stdc++.h>
using namespace std;

using i64 = long long;

#define MOD 998244353
#define INF 0x7f7f7f7f
#define INFLL 0x7f7f7f7f7f7f7f7fLL

void solve() {
	i64 n, k;
	cin >> n >> k;
	i64 cur = 1, cnt = 1, x = k - 1;
	while(cur + x * cnt <= n){
		cur += cnt * x;
		cnt = (cur - 1) / (k - 1) + 1;
		x = (cnt * k - cur) / cnt;
	}
	x = (n - cur) / cnt;
	cout << cur + x * cnt<< '\n';
}

signed main() {
	ios::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
	int _ = 1;
	cin >> _;
	while (_ --){
		solve();
	}
}