#include <bits/stdc++.h>
using namespace std;

using i64 = long long;

#define MOD 998244353
#define INF 0x7f7f7f7f
#define INFLL 0x7f7f7f7f7f7f7f7fLL

void solve() {
	i64 r0, x, d, n;
	cin >> r0 >> x >> d >> n;
	string s;
	cin >> s;
	i64 cur = r0;
	int ans = 0;
	for(auto e : s){
		if(e == '1'){
			cur = max(0ll, cur - d);
			ans ++;
		}else{
			ans += (cur < x);
		}
	}
	cout << ans << '\n';
}

signed main() {
	ios::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
	int _ = 1;
	cin >> _;
	while (_ --){
		solve();
	}
}